# Function to greet a user
def greet(name):
    # Print a greeting with the provided name
    print("Hello, " + name + "!")

# Function to calculate the square of a number
def square(num):
    # Multiply the number by itself and return the result
    return num * num

# Main block of the program

greet("Alice")

# Calculate the square of 5 and store it
result = square(5)

# Print the result
print("The square of 5 is:", result)
